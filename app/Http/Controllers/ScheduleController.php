    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'name' => 'required',
            'date' => 'required',
            'time' => 'required',
            'data' => 'required'
        ]);

        Schedule::create([
            'name' => $request->name,
            'date' => $request->date,
            'time' => $request->time,
            'data' => $request->data,
        ]);

        return redirect()->route('schedules.index');

    }

